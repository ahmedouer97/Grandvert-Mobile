package com.mycompany.Entite;

import java.util.Date;




/**
 *
 * @author psn
 */
public class Jardin {
    
    private int id;
    private int user;
    private String nom;
    private Date dateC;

    public Jardin() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUser() {
        return user;
    }

    public void setUser(int user) {
        this.user = user;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public Date getDateC() {
        return dateC;
    }

    public void setDateC(Date dateC) {
        this.dateC = dateC;
    }

    
    
    
}
